#ifndef COLLISION_H
#define COLLISION_H


#include "Body.h"

////////////////////////////////////////////////////////////////////////////////////////
//	class to store information about each collision
////////////////////////////////////////////////////////////////////////////////////////

class CollisionInfo{
public:
	Body* a;
	Body* b;
	
	Vec2 normal;
	Vec2 contactPoint1;
	Vec2 contactPoint2;
	
	float depth;
};

//////////////////////////////////////////////////////////////////////////////////////////
//	class to handle collision detection between objects
/////////////////////////////////////////////////////////////////////////////////////////

class CollisionDetection{
public:
	static bool isColliding(Body* a, Body* b, CollisionInfo& ci);
	static bool isCollidingCircleCircle(Body* a, Body* b, CollisionInfo& ci);
};



#endif
